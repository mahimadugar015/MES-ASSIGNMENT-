ORG 0000H
;MOVE VALUES IN RAM
MOV 50H, #08H
MOV 51H, #12H
;COMPARE
MOV R0, 50H       
MOV R1, 51H       

CHECK:
MOV A, R0
JZ R0_ZERO      
DEC R0
DEC R1
SJMP CHECK

R0_ZERO:
MOV A, R1
JZ EQUAL         
MOV 52H, #0FFH  
SJMP DONE

EQUAL:
MOV 52H, #00H    
SJMP DONE

GREATER:
MOV 52H, #01H   

DONE:
END
